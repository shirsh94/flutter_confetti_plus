/// High-level confetti motion modes for [ConfettiPlus.launchMode].
enum ConfettiMode {
  /// Staggered upward blasts from both sides.
  fireworks,

  /// A continuous upward stream from the bottom center.
  fountain,

  /// Confetti falling from the top edge.
  rain,

  /// A radial burst from near the upper center.
  explosion,
}
